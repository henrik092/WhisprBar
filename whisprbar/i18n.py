def get_language(config: Mapping[str, object] | None = None) -> str:
    """Return the supported UI language for a config mapping."""
    if not isinstance(config, Mapping):
        return DEFAULT_LANGUAGE
    language = str(config.get("language") or DEFAULT_LANGUAGE).strip().lower()
    return language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def t(key: str, config: Mapping[str, object] | str | None = None) -> str:
    """Translate a UI text key using config language, falling back to English."""
    if isinstance(config, str):
        language = config if config in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE
    else:
        language = get_language(config)
    return TRANSLATIONS.get(language, {}).get(key) or TRANSLATIONS["en"].get(key) or key


def hotkey_action_labels(config: Mapping[str, object] | None = None) -> dict[str, str]:
    """Return localized labels for editable hotkey actions."""
    from whisprbar.hotkey_actions import HOTKEY_ACTION_ORDER

    return {action: t(f"hotkey.{action}", config) for action in HOTKEY_ACTION_ORDER}
